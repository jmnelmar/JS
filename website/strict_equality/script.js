// = assigmnent operator
// == comparison operator (compare if values are equal)
// === strict equality operator (compare if values  & datatye are equal)
// != inequality operator
// !== strict inequality operator

const PI = 3.14;

if (PI == "3.14"){
    console.log("that is pi");
}else{
    console.log("not pi");
}

if (PI === "3.14"){
    console.log("that is pi");
}else{
    console.log("not pi");
}

if (PI != "3.14"){
    console.log("that is pi");
}else{
    console.log("not pi");
}

if (PI !== "3.14"){
    console.log("that is pi");
}else{
    console.log("not pi");
}